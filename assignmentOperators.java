public class assignmentOperators {
    public static void main(String args[]) {
        int b = 10, a;
        a = b;
        System.out.println(a += 1);
        System.out.println(a -= 1);
        System.out.println(a *= 1);
        System.out.println(a /= 1);
        System.out.println(a %= 1);
    }
}
